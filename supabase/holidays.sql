create table if not exists public.company_holidays (
  day date primary key,
  name text not null
);

alter table public.company_holidays enable row level security;

drop policy if exists holidays_select on public.company_holidays;
drop policy if exists holidays_admin_write on public.company_holidays;

-- 로그인 사용자는 조회, 관리자만 쓰기
create policy holidays_select on public.company_holidays for select
  using (auth.uid() is not null);
create policy holidays_admin_write on public.company_holidays for all
  using (public.is_admin()) with check (public.is_admin());

