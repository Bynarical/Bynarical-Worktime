-- RLS 활성화
-- ============================================================
alter table public.profiles enable row level security;
alter table public.workplaces enable row level security;
alter table public.app_settings enable row level security;
alter table public.records enable row level security;
alter table public.leaves enable row level security;
alter table public.confirmations enable row level security;
alter table public.leave_adjustments enable row level security;

-- 정책 재실행 안전: 기존 정책 제거 후 생성
do $$
declare r record;
begin
  for r in
    select policyname, tablename from pg_policies
    where schemaname = 'public'
      and tablename in ('profiles','workplaces','app_settings','records','leaves','confirmations','leave_adjustments')
  loop
    execute format('drop policy if exists %I on public.%I', r.policyname, r.tablename);
  end loop;
end $$;

-- profiles: 본인/관리자 조회, 본인 생성/수정, 관리자 수정
create policy profiles_select on public.profiles for select
  using (id = auth.uid() or public.is_admin());
create policy profiles_insert on public.profiles for insert
  with check (id = auth.uid());
create policy profiles_update on public.profiles for update
  using (id = auth.uid() or public.is_admin())
  with check (id = auth.uid() or public.is_admin());

-- records: 본인 전체 권한 + 관리자 조회
create policy records_owner_all on public.records for all
  using (user_id = auth.uid()) with check (user_id = auth.uid());
create policy records_admin_select on public.records for select
  using (public.is_admin());

-- leaves: 본인 전체 권한 + 관리자 조회/수정(승인)
create policy leaves_owner_all on public.leaves for all
  using (user_id = auth.uid()) with check (user_id = auth.uid());
create policy leaves_admin_select on public.leaves for select
  using (public.is_admin());
create policy leaves_admin_update on public.leaves for update
  using (public.is_admin()) with check (public.is_admin());

-- confirmations: 본인 전체 권한 + 관리자 조회
create policy confirmations_owner_all on public.confirmations for all
  using (user_id = auth.uid()) with check (user_id = auth.uid());
create policy confirmations_admin_select on public.confirmations for select
  using (public.is_admin());

-- workplaces: 로그인 사용자 조회, 관리자만 쓰기
create policy workplaces_select on public.workplaces for select
  using (auth.uid() is not null);
create policy workplaces_admin_write on public.workplaces for all
  using (public.is_admin()) with check (public.is_admin());

-- app_settings: 로그인 사용자 조회, 관리자만 쓰기
create policy settings_select on public.app_settings for select
  using (auth.uid() is not null);
create policy settings_admin_write on public.app_settings for all
  using (public.is_admin()) with check (public.is_admin());

-- leave_adjustments: 본인/관리자 조회, 관리자만 쓰기
create policy adj_select on public.leave_adjustments for select
  using (user_id = auth.uid() or public.is_admin());
create policy adj_admin_write on public.leave_adjustments for all
  using (public.is_admin()) with check (public.is_admin());

