-- ---------- 1) 프로필 (auth.users 1:1) ----------
create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  name text not null,
  emp_no text,
  hire_date date,
  is_admin boolean not null default false,
  created_at timestamptz not null default now()
);

-- ---------- 2) 근무지 (전역, 관리자 관리) ----------
create table if not exists public.workplaces (
  id text primary key,
  name text not null,
  lat double precision not null,
  lng double precision not null,
  radius integer not null default 150
);

-- ---------- 3) 앱 정책 (단일 행) ----------
create table if not exists public.app_settings (
  id integer primary key default 1,
  work_policy jsonb,
  leave_policy jsonb,
  updated_at timestamptz default now(),
  constraint app_settings_singleton check (id = 1)
);

-- ---------- 4) 근태 기록 ----------
create table if not exists public.records (
  id text primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  date date not null,
  planned_start text,
  check_in timestamptz,
  check_out timestamptz,
  type text not null default 'WORK',
  workplace_id text,
  workplace_name text,
  in_lat double precision,
  in_lng double precision,
  in_verified boolean,
  out_lat double precision,
  out_lng double precision,
  out_verified boolean,
  note text,
  hash text,
  prev_hash text,
  updated_at timestamptz default now(),
  unique (user_id, date)
);

-- ---------- 5) 연차 ----------
create table if not exists public.leaves (
  id text primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  date date not null,
  hours integer not null,
  segment text not null,
  start_time text,
  end_time text,
  reason text,
  status text not null default 'REQUESTED',
  requested_at timestamptz default now(),
  decided_at timestamptz,
  decided_by text,
  decision_note text,
  hash text,
  prev_hash text
);

-- ---------- 6) 주간 확인(전자서명) ----------
create table if not exists public.confirmations (
  id text primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  week_start date not null,
  week_end date not null,
  signature text,
  total_worked_minutes integer default 0,
  summary_hash text,
  hash text,
  prev_hash text,
  confirmed_at timestamptz default now(),
  unique (user_id, week_start)
);

-- ---------- 7) 연차 수동 조정 ----------
create table if not exists public.leave_adjustments (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  hours numeric not null,
  note text,
  created_by uuid,
  at timestamptz default now()
);

-- ============================================================
-- 헬퍼 함수: 현재 사용자가 관리자인지 (RLS 재귀 방지 위해 SECURITY DEFINER)
-- ============================================================
create or replace function public.is_admin()
returns boolean
language sql
security definer
stable
set search_path = public
as $$
  select coalesce((select is_admin from public.profiles where id = auth.uid()), false);
$$;

-- 이름 → 로그인 이메일 조회 (직원은 이름+비밀번호로 로그인).
-- 정확히 1명일 때만 이메일 반환(동명이인/없음 → null). anon도 호출 가능해야 로그인 화면에서 사용.
create or replace function public.login_email_for_name(p_name text)
returns text
language plpgsql
security definer
stable
set search_path = public, auth
as $$
declare c int; e text;
begin
  select count(*) into c from public.profiles where trim(name) = trim(p_name);
  if c <> 1 then return null; end if;
  select u.email into e
    from auth.users u join public.profiles pr on pr.id = u.id
    where trim(pr.name) = trim(p_name);
  return e;
end;
$$;
grant execute on function public.login_email_for_name(text) to anon, authenticated;

-- ============================================================
-- 가입 시 프로필 자동 생성 (회원가입 메타데이터에서 이름/사번 등 추출)
-- ============================================================
create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (id, name, emp_no, hire_date, is_admin)
  values (
    new.id,
    coalesce(new.raw_user_meta_data->>'name', split_part(new.email, '@', 1)),
    nullif(new.raw_user_meta_data->>'emp_no', ''),
    nullif(new.raw_user_meta_data->>'hire_date', '')::date,
    coalesce((new.raw_user_meta_data->>'is_admin')::boolean, false)
  )
  on conflict (id) do nothing;
  return new;
end;
$$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
  after insert on auth.users
  for each row execute function public.handle_new_user();

